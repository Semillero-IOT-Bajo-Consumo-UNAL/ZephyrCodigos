import re
import math

# Frecuencia para la nota La4 (MIDI 69)
A4_FREQ = 440.0
A4_MIDI = 69

# Mapeo nombre nota -> semitonos (C=0)
note_map = {
    'C': 0, 'C#':1, 'Db':1,
    'D': 2, 'D#':3, 'Eb':3,
    'E': 4,
    'F': 5, 'F#':6, 'Gb':6,
    'G': 7, 'G#':8, 'Ab':8,
    'A': 9, 'A#':10, 'Bb':10,
    'B': 11
}

# Mapeo para nombres en español simplificado (Do, Re, Mi,...)
spanish_names = ['Do', 'Do#', 'Re', 'Re#', 'Mi', 'Fa', 'Fa#', 'Sol', 'Sol#', 'La', 'La#', 'Si']

def note_to_midi(note_str):
    # Ejemplo: Gb1, C4, Db4, etc
    match = re.match(r"([A-G][b#]?)(-?\d+)", note_str)
    if not match:
        return None
    note, octave = match.groups()
    octave = int(octave)
    semitone = note_map[note]
    midi_num = (octave + 1) * 12 + semitone
    return midi_num

def midi_to_freq(midi_num):
    return round(A4_FREQ * 2 ** ((midi_num - A4_MIDI) / 12), 1)

def midi_to_spanish_name(midi_num):
    note_idx = midi_num % 12
    octave = (midi_num // 12) - 1
    name = spanish_names[note_idx]
    # Añadir + para octavas altas si quieres
    if octave > 4:
        name += "+"
    return name

